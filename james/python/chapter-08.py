    # Chapter 8: Concatenating text strings

# You can combine two text strings together like this
x = "Hello, "
y = "World!"
xy = x + y 
# xy = "Hello, World!"
# This is called concatenation

# You can do a combination of variables and strings
comma = ","
exclam = "!"
xyz = "Hello" + comma + "World" + exclam

# You cant concatenate strings and numbers
print ("the sum of 2 + 2 is " + 4)
# This wouldn't work, but this would:
print ("the sum of 2 + 2 is " + "4")
