# A tuple is a list, but the elements are fixed.
# Its the same with a list, but with normal brackets, not square.
countries_in_UK = ("England", "Scotland", "Wales", "Northern Ireland")

# You pick a specific element out of a tuple the same way as you do in a list
print(countries_in_UK[1])

# A tuple doesnt allow you to make any changes to the list. 
# You cant add, modify, remove, delete or pop.
# To make any changes, you have to recode the part you want to change.