# A function is a block of code that does the same thing every time you invoke its name.
# It saves repetitive coding

# If you had this
first_number = 2
second_number = 3
total = first_number + second_number
print(total)

# To make this a function, we use def (function name()):
def add_numbers():
    first_number = 2
    second_number = 3
    total = first_number + second_number
    print(total)
